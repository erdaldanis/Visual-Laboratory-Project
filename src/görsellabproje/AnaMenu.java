    private void KaydetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_KaydetActionPerformed
        OgrenciListe.setText(OgrenciListe.getText()+Ad.getText()+" "+Soyad.getText()+" "+Numara.getText()+" "+Eposta.getText()+" "+Adres.getText()+"\n");
        ogrenci ogrenciNesnesi=new ogrenci();
        ogrenciNesnesi.setOgrenciAdi(Ad.getText());
        ogrenciNesnesi.setOgrenciSoyadi(Soyad.getText());
        ogrenciNesnesi.setOgrenciNumarasi(Numara.getText());
        ogrenciNesnesi.setOgrenciEposta(Eposta.getText());
        ogrenciNesnesi.setOgrenciAdres(Adres.getText());
        ogrenciListesi.add(ogrenciNesnesi);
    }//GEN-LAST:event_KaydetActionPerformed

