    private void Kaydet1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Kaydet1ActionPerformed

        int sayac=0;
        for (int i = 0; i < ogrenciListesi.size(); i++)
        {
            if(ogrenciListesi.get(i).getOgrenciNumarasi().equalsIgnoreCase(ÖgrNo.getText()))
            {
                sayac++;
            }
        }
        if(sayac>0)
        {
            NotListe.setText("\n"+ÖgrNo.getText()+" "+Ders.getSelectedItem()+" "+NotunuGir.getText()+NotListe.getText());
            notlar notlarNesnesi=new notlar((String)Ders.getSelectedItem(),NotunuGir.getText(),ÖgrNo.getText());
            notlarListesi.add(notlarNesnesi);

        }
        else
        {
            JOptionPane.showMessageDialog(null,"Sistemimize kayıtlı böyle bir öğrenci bulunamadı.","Error 404",JOptionPane.ERROR_MESSAGE);
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_Kaydet1ActionPerformed

    private void ÜnlemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ÜnlemActionPerformed
        String erdal= DersAd.getText();
        StringTokenizer st =new StringTokenizer(erdal," ");
        if(sayac==0)
        {
            while(st.hasMoreElements())
            {
                Ders.addItem(st.nextToken());

            }
            sayac++;
        }
        else
        {
            while(st.hasMoreElements())
            {
                Ders.removeItem(st.nextToken());

            }

            sayac=0;
        }
    }//GEN-LAST:event_ÜnlemActionPerformed

